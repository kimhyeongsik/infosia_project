package struts.Model;

public class WorkingDayVO {
	
	private String regdata;
	private String inwork;
	private String outwork;
	private String overtime;
	
	public String getRegdata() {
		return regdata;
	}
	public void setRegdata(String regdata) {
		this.regdata = regdata;
	}
	public String getInwork() {
		return inwork;
	}
	public void setInwork(String inwork) {
		this.inwork = inwork;
	}
	public String getOutwork() {
		return outwork;
	}
	public void setOutwork(String outwork) {
		this.outwork = outwork;
	}
	public String getOvertime() {
		return overtime;
	}
	public void setOvertime(String overtime) {
		this.overtime = overtime;
	}
	
	

}
